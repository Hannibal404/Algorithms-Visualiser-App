    int n = sizeof(arr)/sizeof(arr[0]);
 
    cout << "Array before sorting: ";
    printArray(arr, n);
 
    shellSort(arr, n);
 
    cout << "\Array after sorting: ";
    printArray(arr, n);
 
    return 0;
}
